# all Flask routes below
@app.route("/")
def home():
    return render_template("index.html")


@app.route('/add', methods=['GET', 'POST'])
def add_cafe():
    form = CafeForm()
    if form.validate_on_submit():
        # List that we want to add as a new row
        new_data = [form.cafe.data, form.location_url.data, form.opening_time.data.strftime("%I:%M%p"),
                    form.closing_time.data.strftime("%I:%M%p"),
                    form.coffee_rating.data,
                    form.wifi_rating.data, form.power_rating.data]
        # Open our existing CSV file in append mode
        # Create a file object for this file
        with open('cafe-data.csv', 'a', newline='', encoding='utf8') as f_object:
            # Pass this file object to csv.writer()
            # and get a writer object
            writer_object = writer(f_object)

            # Pass the list as an argument into
            # the writerow()
            writer_object.writerow(new_data)

            # Close the file object
            f_object.close()
        return redirect(url_for('cafes'))
    # Exercise:
    # Make the form write a new row into cafe-data.csv
    # with   if form.validate_on_submit()
    return render_template('add.html', form=form)


@app.route('/cafes')
def cafes():
    with open('cafe-data.csv', newline='', encoding='utf8') as csv_file:
        csv_data = csv.reader(csv_file, delimiter=',')
        list_of_rows = []
        for row in csv_data:
            list_of_rows.append(row)
    return render_template('cafes.html', cafes=list_of_rows)


